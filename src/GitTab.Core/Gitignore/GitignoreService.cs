    public IReadOnlyList<GitignoreTemplate> Templates { get; } = BuildTemplateList();

    public IReadOnlyList<string> Detect(string workingDir)
    {
        var found = new List<string>();
        if (string.IsNullOrWhiteSpace(workingDir) || !Directory.Exists(workingDir))
            return found;

        foreach (var (key, _, markers, globs) in Detectors)
        {
            if (markers.Any(m => MarkerExists(workingDir, m)) || globs.Any(g => AnyFileMatches(workingDir, g)))
                found.Add(key);
        }
        // The OS/editor "global" template is always a good idea.
        found.Add("global");
        return found;
    }

    public string Build(IEnumerable<string> keys)
    {
        var byKey = Templates.ToDictionary(t => t.Key, StringComparer.Ordinal);
        var sb = new StringBuilder();
        sb.Append("# Generated by Git Tab\n");
        foreach (var key in keys.Distinct())
        {
            if (!byKey.TryGetValue(key, out var t)) continue;
            sb.Append('\n').Append("### ").Append(t.Name).Append(" ###\n").Append(t.Content.TrimEnd('\n')).Append('\n');
        }
        return sb.ToString();
    }

    public string? ReadExisting(string workingDir)
    {
        var path = Path.Combine(workingDir, ".gitignore");
        return File.Exists(path) ? File.ReadAllText(path) : null;
    }

    public void Write(string workingDir, string content, bool append)
    {
        var path = Path.Combine(workingDir, ".gitignore");
        if (append && File.Exists(path))
        {
            var existing = File.ReadAllText(path).TrimEnd('\n');
            File.WriteAllText(path, existing + "\n\n" + content.TrimStart('\n'));
        }
        else
        {
            File.WriteAllText(path, content);
        }
    }

    // ---- detection helpers (bounded depth, skips heavy dirs) ----

    private static readonly HashSet<string> SkipDirs = new(StringComparer.OrdinalIgnoreCase)
        { ".git", "node_modules", "bin", "obj", ".vs", ".idea", "target", "build", "dist", ".gradle" };

    private static bool MarkerExists(string root, string marker)
    {
        if (marker.Contains('/'))
            return File.Exists(Path.Combine(root, marker.Replace('/', Path.DirectorySeparatorChar)));
        if (marker.Contains('*'))
            return AnyFileMatches(root, marker);
        return File.Exists(Path.Combine(root, marker));
    }

    private static bool AnyFileMatches(string root, string glob, int maxDepth = 3)
    {
        var queue = new Queue<(string Dir, int Depth)>();
        queue.Enqueue((root, 0));
        while (queue.Count > 0)
        {
            var (dir, depth) = queue.Dequeue();
            try
            {
                if (Directory.EnumerateFiles(dir, glob, SearchOption.TopDirectoryOnly).Any())
                    return true;
                if (depth < maxDepth)
                {
                    foreach (var sub in Directory.EnumerateDirectories(dir))
                    {
                        var name = Path.GetFileName(sub);
                        if (!SkipDirs.Contains(name) && !name.StartsWith('.'))
                            queue.Enqueue((sub, depth + 1));
                    }
                }
            }
            catch (UnauthorizedAccessException) { /* skip */ }
            catch (IOException) { /* skip */ }
        }
        return false;
    }

