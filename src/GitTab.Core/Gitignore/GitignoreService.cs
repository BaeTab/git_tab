using System.Text;

namespace GitTab.Core.Gitignore;

public sealed class GitignoreTemplate
{
    public required string Key { get; init; }
    public required string Name { get; init; }
    public required string Content { get; init; }
}

/// <summary>Generates a .gitignore from bundled, stack-specific templates, with auto-detection.</summary>
public interface IGitignoreService
{
    IReadOnlyList<GitignoreTemplate> Templates { get; }

    /// <summary>Template keys whose marker files are present under <paramref name="workingDir"/>.</summary>
    IReadOnlyList<string> Detect(string workingDir);

    /// <summary>Concatenates the selected templates into a single .gitignore body.</summary>
    string Build(IEnumerable<string> keys);

    string? ReadExisting(string workingDir);

    /// <summary>Writes (or appends) the content to <c>{workingDir}/.gitignore</c>.</summary>
    void Write(string workingDir, string content, bool append);
}

public sealed class GitignoreService : IGitignoreService
{
    private static readonly (string Key, string Name, string[] Markers, string[] MarkerGlobs)[] Detectors =
    {
        ("dotnet", ".NET / Visual Studio", new[] { "*.sln", "*.csproj", "*.fsproj", "*.vbproj" }, Array.Empty<string>()),
